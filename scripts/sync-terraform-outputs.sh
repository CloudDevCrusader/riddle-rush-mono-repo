#!/bin/bash

# Script to sync Terraform outputs to JSON files and environment variables
# This makes Terraform outputs available to Nuxt.js and deployment scripts

set -e

ENVIRONMENT="${1:-production}"

# Map short environment names to full folder names
case "$ENVIRONMENT" in
    dev)
        ENVIRONMENT="development"
        ;;
    prod|production)
        ENVIRONMENT="production"
        ;;
    staging)
        ENVIRONMENT="staging"
        ;;
esac

INFRA_DIR="infrastructure/environments/${ENVIRONMENT}"

# Colors
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
RED='\033[0;31m'
NC='\033[0m' # No Color

echo -e "${BLUE}ðŸ”„ Syncing Terraform outputs for ${ENVIRONMENT}...${NC}"

# Check if directory exists
if [ ! -d "$INFRA_DIR" ]; then
    echo -e "${RED}âŒ Environment directory not found: ${INFRA_DIR}${NC}"
    exit 1
fi

# Check if terraform is available
if ! command -v terraform &> /dev/null; then
    echo -e "${RED}âŒ Terraform not found. Run 'pnpm run infra:setup' first${NC}"
    exit 1
fi

# Navigate to environment directory
cd "$INFRA_DIR"

# Check if initialized
if [ ! -d ".terraform" ]; then
    echo -e "${YELLOW}âš ï¸  Terraform not initialized. Running init...${NC}"
    terraform init
fi

# Export outputs to JSON file
echo -e "${BLUE}Exporting outputs to JSON...${NC}"
terraform output -json > terraform-outputs.json 2>/dev/null || {
    echo -e "${YELLOW}âš ï¸  No outputs found. Run 'terraform apply' first${NC}"
    exit 1
}

# Create .env file with outputs
echo -e "${BLUE}Creating .env file...${NC}"
cat > .env.terraform << EOF
# Terraform outputs for ${ENVIRONMENT} environment
# Generated by sync-terraform-outputs.sh
# DO NOT EDIT MANUALLY - This file is auto-generated

AWS_S3_BUCKET=$(terraform output -raw bucket_name 2>/dev/null || echo "")
AWS_CLOUDFRONT_ID=$(terraform output -raw cloudfront_distribution_id 2>/dev/null || echo "")
AWS_REGION=$(terraform output -raw aws_region 2>/dev/null || echo "eu-central-1")
CLOUDFRONT_DOMAIN=$(terraform output -raw cloudfront_domain_name 2>/dev/null || echo "")
WEBSITE_URL=$(terraform output -raw website_url 2>/dev/null || echo "")

# For Nuxt.js runtime config
NUXT_PUBLIC_CLOUDFRONT_DOMAIN=$(terraform output -raw cloudfront_domain_name 2>/dev/null || echo "")
NUXT_PUBLIC_WEBSITE_URL=$(terraform output -raw website_url 2>/dev/null || echo "")
EOF

# Go back to project root
cd - > /dev/null

echo -e "\n${GREEN}âœ… Terraform outputs synced!${NC}"
echo -e "${BLUE}Files created:${NC}"
echo -e "  - ${INFRA_DIR}/terraform-outputs.json"
echo -e "  - ${INFRA_DIR}/.env.terraform"
echo ""
echo -e "${BLUE}ðŸ’¡ To use in Nuxt.js:${NC}"
echo -e "  ${YELLOW}source ${INFRA_DIR}/.env.terraform${NC}"
echo ""
echo -e "${BLUE}ðŸ’¡ To use in deployment:${NC}"
echo -e "  ${YELLOW}source ${INFRA_DIR}/.env.terraform && ./scripts/aws-deploy.sh ${ENVIRONMENT}${NC}"

